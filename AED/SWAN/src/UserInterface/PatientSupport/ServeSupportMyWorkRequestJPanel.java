    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String searchText = txtSearch.getText().trim();
        boolean boolsearch = Validate.isNumeric(searchText);
        if(!searchText.isEmpty())
        {
            if(boolsearch)
            {
                txtSearch.setBackground(Color.WHITE);
                DefaultTableModel model = (DefaultTableModel)tblTicket.getModel();

                model.setRowCount(0);
                for(WorkRequest request : userAccount.getWorkQueue().getWorkRequestList())
                {
                    if(request.getTicketNumber()== Integer.parseInt(searchText))
                    {
                        Object[] row = new Object[6];
                        row[0] = request.getTicketNumber();
                        row[1] = request;
                        row[2] = ((SupportWorkRequest) request).getSender() == null? null :((SupportWorkRequest) request).getSender().getPatient();
                        row[3] = sdf.format(request.getRequestDate());
                        row[4] = request.getStatus();
                        row[5] = ((SupportWorkRequest) request).getOrganization();
        
                          model.addRow(row);
                    }
                }
                txtSearch.setText("");
            }
            else
            {
                if(!boolsearch)
                {
                    txtSearch.setBackground(Color.red);
                }
                else
                txtSearch.setBackground(Color.WHITE);
            }

        }
        else
        {
            populateTable();
        }
    }//GEN-LAST:event_btnSearchActionPerformed


