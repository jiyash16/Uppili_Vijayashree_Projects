    public void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) tblTicket.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
             if(request instanceof SupportWorkRequest)
           {
            Object[] row = new Object[6];
            row[0] = request.getTicketNumber();
            row[1] = request;
            row[2] = ((SupportWorkRequest) request).getSender() == null? null :((SupportWorkRequest) request).getSender().getPatient();
            row[3] = sdf.format(request.getRequestDate());
            row[4] = request.getStatus();
            row[5] = ((SupportWorkRequest) request).getOrganization();
           
           
           
            model.addRow(row);
           }
        }
    }
