    public void populateTable()
    {
        
        DefaultTableModel dtm = (DefaultTableModel)tblTimeSheet.getModel();
        dtm.setRowCount(0);
        boolean daypresent = false;
        for(TimeSheet ts : userAccount.getEmployee().getTimeSheetDirectory().getTimesheetDirectory())
        {
            daypresent = false;
            Object[] row = new Object[4];
            if(DateUtils.isToday(ts.getToday()))
            {
                daypresent = true;
            }
            row[0] = sdf.format(ts.getToday());
            if(ts.isPresent())
            {
                row[1] = "Present";
            }
            else
            {
                row[1] = "Absent";
            }
            row[2] = ts;
            if(ts.isWFH())
            {
                row[3] = "Yes";
            }
            else
            {
                row[3] = "No";
            }
            dtm.addRow(row);
            
        }
        if(daypresent)
        {
            btnAdd.setVisible(false);
        }
        else
        {
            btnAdd.setVisible(true);
        }
    }

