    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
      try
      {
        Date date = new Date();
        String date1 = sdf.format(date);
        String duration = txtDuration.getText().trim();
        boolean present = false;
        boolean wfh = false;
        if(chkPresent.isSelected())
        {
            present = true;
        }
        if(chkWFH.isSelected())
        {
            wfh = true;
        }
        if(!duration.isEmpty())
        {
            if(Validate.isNumeric(duration))
            {
                if(Integer.parseInt(duration) <= 20)
                {
                        if(wfhmain==1 && wfh==false)
                        {
                            JOptionPane.showMessageDialog(null,"You have not selected Work from home","Error",JOptionPane.ERROR_MESSAGE);
                            return;
                        }
                        txtDuration.setBackground(Color.WHITE);
                        TimeSheet ts = userAccount.getEmployee().getTimeSheetDirectory().fillTimesheet();
                        ts.setDuration(Integer.parseInt(duration));
                        ts.setPresent(present);
                        ts.setWFH(wfh);
                        ts.setToday(sdf.parse(date1));
                        JOptionPane.showMessageDialog(null,"TimeSheet added succesfully","Succes",JOptionPane.INFORMATION_MESSAGE);
                        txtDuration.setText("");
                        chkPresent.setSelected(false);
                        chkWFH.setSelected(false);
                        populateTable();
                        AddTimeSheetJPanel.setVisible(false);
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Duration Cannot be greater than 20","Warning",1);
                   
                            
                }
            }
            else
            {
                txtDuration.setBackground(Color.red);
                JOptionPane.showMessageDialog(null,"Invalid data","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Fields cannot be empty","Information",JOptionPane.INFORMATION_MESSAGE);

        }
      }
      catch(Exception e)
      {
          System.out.println("Error date");
      }
    }//GEN-LAST:event_btnSaveActionPerformed

