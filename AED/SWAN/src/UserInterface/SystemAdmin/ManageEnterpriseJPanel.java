    public void populateCombo()
    {
        cmbNetwork.removeAllItems();
        cmbEnterpriseType.removeAllItems();

        for (Network network : system.getNetworkList()) {
            cmbNetwork.addItem(network);
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            cmbEnterpriseType.addItem(type);
        }
    }
    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblEnterprise.getModel();
        dtm.setRowCount(0);
        for(Network nw: system.getNetworkList())
        {
            for(Enterprise e: nw.getEnterpriseDirectory().getEnterpriseList())
            {
                Object row[] = new Object[6];
                row[0]= e.getEnterpriseType();
                row[1] = nw;
                row[2] = e;
                row[3] = e.getLocation();
                row[4] = sdf.format(e.getCreatedOn());
                if(e.getModifiedOn() == null)
                {
                    row[5] = "";
                }
                else
                {
                    row[5] = sdf.format(e.getModifiedOn());
                }
                dtm.addRow(row);
            }
        }
    }
