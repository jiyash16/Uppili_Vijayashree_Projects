    private void btnUpdateSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateSaveActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblEnterprise.getSelectedRow();
        
        Enterprise e = (Enterprise) tblEnterprise.getValueAt(selectedrow, 2);
        String enterpriseName = txtEnterpriseName.getText().trim();
        String enterpriseloc = txtLocation.getText().trim();
        String enterpriseLoc = txtLocation.getText().trim();
        boolean boolenterpriseName = Validate.isalphanumeric(enterpriseName);
        boolean boolenterpriseLc = Validate.isalphanumeric(enterpriseLoc);
        if(!enterpriseName.isEmpty() && !enterpriseloc.isEmpty())
        {
            if(boolenterpriseName && boolenterpriseLc)
            {
                txtLocation.setBackground(Color.WHITE);
                txtEnterpriseName.setBackground(Color.WHITE);
            
                e.setName(enterpriseName);
                e.setLocation(enterpriseloc);

                e.setModifiedOn(new Date());
                JOptionPane.showMessageDialog(null,"Enterprise details updated successfully","Success",JOptionPane.INFORMATION_MESSAGE);
                populateTable();
                AddEnterpriseJPanel.setVisible(false);
            }
            else
            {
                if(!boolenterpriseLc)
                {
                    txtLocation.setBackground(Color.red);
                    
                }
                if(!boolenterpriseName)
                {
                    txtEnterpriseName.setBackground(Color.red);
                }
                JOptionPane.showMessageDialog(null,"Invalid data","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Fields cannot be empty","Warning",2);
        }
    }//GEN-LAST:event_btnUpdateSaveActionPerformed


