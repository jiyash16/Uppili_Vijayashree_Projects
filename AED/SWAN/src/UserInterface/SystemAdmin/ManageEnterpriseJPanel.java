    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        btnUpdate.setVisible(false);
        //Date date = new Date();
        String enterpriseName = txtEnterpriseName.getText().trim();
        String enterpriseLoc = txtLocation.getText().trim();
        boolean boolenterpriseName = Validate.isalphanumeric(enterpriseName);
        boolean boolenterpriseLc = Validate.isalphanumeric(enterpriseLoc);
        if(!enterpriseName.isEmpty() && !enterpriseLoc.isEmpty() && cmbEnterpriseType.getSelectedItem() != null && cmbNetwork.getSelectedItem() != null)
        {
            if(boolenterpriseName && boolenterpriseLc)
            {
                txtLocation.setBackground(Color.WHITE);
                txtEnterpriseName.setBackground(Color.WHITE);
                Network nw = (Network) cmbNetwork.getSelectedItem();
                Enterprise e = nw.getEnterpriseDirectory().createAndAddEnterprise(enterpriseName,(Enterprise.EnterpriseType) cmbEnterpriseType.getSelectedItem());     
                e.setCreatedOn(new Date());
                e.setLocation(enterpriseLoc);
            
                JOptionPane.showMessageDialog(null,"Enterprise added succesfully","Succes",JOptionPane.INFORMATION_MESSAGE);
                txtEnterpriseName.setText("");
                txtLocation.setText("");
                populateTable();
                AddEnterpriseJPanel.setVisible(false);
            }
            else
            {
                if(!boolenterpriseLc)
                {
                    txtLocation.setBackground(Color.red);
                    
                }
                if(!boolenterpriseName)
                {
                    txtEnterpriseName.setBackground(Color.red);
                }
                JOptionPane.showMessageDialog(null,"Invalid data","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Fields cannot be empty","Information",JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_btnSaveActionPerformed

