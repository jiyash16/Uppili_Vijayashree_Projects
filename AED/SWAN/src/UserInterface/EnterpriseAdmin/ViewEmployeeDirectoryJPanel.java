    public void populateTable(Organization o)
    {
        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
       
        model.setRowCount(0);
        
        for (Employee e: o.getEmployeeDirectory().getEmployeeList()){
            Object[] row = new Object[3];
            row[0] = e.getEmployeeId();
            row[1] = e;
            
            if(e.isStatus())
                row[2] = "Active";
            else
                row[2] ="InActive";

            
            model.addRow(row);
        }
    }

