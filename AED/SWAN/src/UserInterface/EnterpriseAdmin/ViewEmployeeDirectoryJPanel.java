    private void btnViewProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewProfileActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblEmployee.getSelectedRow();
        if(selectedrow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select any row to edit","Information",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Employee e = (Employee) tblEmployee.getValueAt(selectedrow, 1);
        ViewUpdatePersonDetailsJPanel viewEmployeedetails = new ViewUpdatePersonDetailsJPanel(userProcessContainer,e,null ,"employee","view",0);
        userProcessContainer.add("ViewUpdatePersonDetailsJPanel",viewEmployeedetails);
        CardLayout cl = (CardLayout)userProcessContainer.getLayout();
        cl.next(userProcessContainer);
        
    }//GEN-LAST:event_btnViewProfileActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblEmployee.getSelectedRow();
        if(selectedrow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select any row to edit","Information",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Employee e = (Employee) tblEmployee.getValueAt(selectedrow, 1);
        e.setStatus(false);
        JOptionPane.showMessageDialog(null,"Employee disabled successfully","Succes",1);
        populateTable((Organization) cmbOrganization.getSelectedItem());
    }//GEN-LAST:event_btnDeleteActionPerformed

