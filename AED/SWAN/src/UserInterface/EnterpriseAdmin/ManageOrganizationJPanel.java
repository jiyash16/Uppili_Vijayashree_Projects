    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String organizationLoc = txtLocation.getText().trim();
        String organizationName = txtOrganizationName.getText().trim();
        boolean boolorganizationLoc = Validate.isString(organizationLoc);
        boolean boolorganizationName = Validate.isalphanumeric(organizationName);
        if( !organizationLoc.isEmpty() && !organizationName.isEmpty() && cmbOrganizationType.getSelectedItem() != null )
        {
            if(boolorganizationLoc && boolorganizationName)
            {
                txtLocation.setBackground(Color.WHITE);
                txtOrganizationName.setBackground(Color.WHITE);
              //  System.out.println("type organization"+cmbOrganizationType.getSelectedItem());
                Organization org = enterprise.getOrganizationDirectory().createOrganization(organizationName,(Type)cmbOrganizationType.getSelectedItem());
                org.setCreatedOn(new Date());
                org.setLocation(organizationLoc);
                org.setStatus(true);
                JOptionPane.showMessageDialog(null,"Organization added succesfully","Succes",JOptionPane.INFORMATION_MESSAGE);
             //   txtOrganizationName1.setText("");
                txtLocation.setText("");
                populateTable();
                AddOrganizationJPanel.setVisible(false);
            }
            else
            {

                if(!boolorganizationLoc)
                {
                    txtLocation.setBackground(Color.red);
                }
                JOptionPane.showMessageDialog(null,"Invalid data","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Fields cannot be empty","Information",JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_btnSaveActionPerformed

