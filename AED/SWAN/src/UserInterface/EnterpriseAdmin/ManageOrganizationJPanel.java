    private void btnUpdateSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateSaveActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblOrganization.getSelectedRow();

        Organization org = (Organization) tblOrganization.getValueAt(selectedrow, 1);
         
        String organizationLoc = txtLocation.getText().trim();
        String organizationName = txtOrganizationName.getText().trim();
        boolean boolorganizationLoc = Validate.isString(organizationLoc);
        boolean boolorganizationName = Validate.isString(organizationName);
        if(!organizationLoc.isEmpty() && !organizationName.isEmpty() && cmbOrganizationType.getSelectedItem() != null )
        {
            if(boolorganizationLoc && boolorganizationName)
            {
               txtLocation.setBackground(Color.WHITE);
               txtOrganizationName.setBackground(Color.WHITE);
                ;

                
                org.setLocation(organizationLoc);
                org.setName(organizationName);
                org.setModifiedOn(new Date());
                JOptionPane.showMessageDialog(null,"Organization details updated successfully","Success",JOptionPane.INFORMATION_MESSAGE);
                populateTable();
                AddOrganizationJPanel.setVisible(false);
            }
            else
            {
                if(!boolorganizationLoc)
                {
                    txtLocation.setBackground(Color.red);

                }
                else
                {
                    txtLocation.setBackground(Color.WHITE);
                }
                if(!boolorganizationName)
                {
                    txtOrganizationName.setBackground(Color.red);

                }
                else
                {
                    txtOrganizationName.setBackground(Color.WHITE);
                }

                JOptionPane.showMessageDialog(null,"Invalid data","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Fields cannot be empty","Warning",2);
        }
    }//GEN-LAST:event_btnUpdateSaveActionPerformed

