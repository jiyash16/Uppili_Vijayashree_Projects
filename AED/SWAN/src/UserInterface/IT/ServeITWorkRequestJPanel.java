    private void btnAssignToMe2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMe2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblITWrokRequest.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        SupportWorkRequest request = (SupportWorkRequest)tblITWrokRequest.getValueAt(selectedRow, 1);
       
        
          String status = (String)tblITWrokRequest.getValueAt(selectedRow, 6);
        
        if(status.equalsIgnoreCase("Pending Request"))
        {
            if(wfh==1)
            {
                Validate.createLogFile(userAccount.getUsername(), "Employee assigned Ticket to himself");
            }
        
             request.setEmployeeReceiver(userAccount);
            request.setStatus("Work in Progress");
            userAccount.getWorkQueue().getWorkRequestList().add(request);
            populateTable();
            
        }
        else
        {
           JOptionPane.showMessageDialog(null,"WorkRequest already assigned","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAssignToMe2ActionPerformed


