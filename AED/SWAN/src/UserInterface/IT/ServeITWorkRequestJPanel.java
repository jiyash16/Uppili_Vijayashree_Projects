    public void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) tblITWrokRequest.getModel();
        
        model.setRowCount(0);
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
           
            Object[] row = new Object[7];
            row[0] = request.getTicketNumber();
            row[1] = request;
            row[2] = ((SupportWorkRequest) request).getAssignedby() == null? null :((SupportWorkRequest) request).getAssignedby().getEmployee();
            row[3] = sdf.format(request.getRequestDate());
            row[4] = ((SupportWorkRequest) request).getPatient() == null? null :((SupportWorkRequest) request).getPatient();
            row[5] = ((SupportWorkRequest) request).getEmployeeReceiver() == null? null :((SupportWorkRequest) request).getEmployeeReceiver().getEmployee();
            row[6] = ((SupportWorkRequest) request).getSwrReturnStatus() == null ? request.getStatus():((SupportWorkRequest) request).getSwrReturnStatus();
            
           
           
           
            model.addRow(row);
           
        }
    }

