    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblITTickets.getSelectedRow();
        if(selectedRow <0)
        {
            JOptionPane.showMessageDialog(null,"Please select any row","Info",1);
            return;
        }
        SupportWorkRequest request = (SupportWorkRequest) tblITTickets.getValueAt(selectedRow, 1);
        if(request.getSwrReturnStatus()!= null)
        {
            if(request.getSwrReturnStatus().equalsIgnoreCase("Returned"))
            {
                JOptionPane.showMessageDialog(null,"Request is returned Cannot process");
            }
             else
        {
            if(wfh==1)
            {
                Validate.createLogFile(userAccount.getUsername(), "Employee Working on ticket");
            }
        ProcessMyRequestITJPanel processMyRequestITJPanel = new ProcessMyRequestITJPanel(userProcessContainer,userAccount, request,wfh);
        userProcessContainer.add("ProcessMyRequestITJPanel",processMyRequestITJPanel);
        CardLayout cl = (CardLayout)userProcessContainer.getLayout();
        cl.next(userProcessContainer);
        }
        }
        else
        {
            if(wfh==1)
            {
                Validate.createLogFile(userAccount.getUsername(), "Employee working on ticket");
            }
        ProcessMyRequestITJPanel processMyRequestITJPanel = new ProcessMyRequestITJPanel(userProcessContainer,userAccount, request,wfh);
        userProcessContainer.add("ProcessMyRequestITJPanel",processMyRequestITJPanel);
        CardLayout cl = (CardLayout)userProcessContainer.getLayout();
        cl.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnProcessActionPerformed

