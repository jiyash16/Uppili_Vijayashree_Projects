    public void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) tblITTickets.getModel();
        
        model.setRowCount(0);
        
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
             if(request instanceof SupportWorkRequest)
           {
            Object[] row = new Object[6];
            row[0] = request.getTicketNumber();
            row[1] = request;
            row[2] = ((SupportWorkRequest) request).getAssignedby() == null? null :((SupportWorkRequest) request).getAssignedby().getEmployee();
            row[3] = sdf.format(request.getRequestDate());
            row[4] = ((SupportWorkRequest) request).getResolveDate() == null? null :sdf.format(((SupportWorkRequest) request).getResolveDate());
            
            row[5] = ((SupportWorkRequest) request).getSwrReturnStatus() == null ? request.getStatus():((SupportWorkRequest) request).getSwrReturnStatus();
            
           
           
           
            model.addRow(row);
           
        }
        }
    }

