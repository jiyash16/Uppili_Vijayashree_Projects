    public void populateTable()
    {
        DefaultTableModel dtm = (DefaultTableModel)tblWFH.getModel();
        dtm.setRowCount(0);
        
        for(IPConfig ip : system.getRmap().getIplist())
        {
            Object[] row = new Object[3];
            row[0] = ip;
            row[1] = ip.getLocation();
            if(ip.getHomeip() ==1)
            {
                row[2] = "Yes";
            }
            else
            {
                 row[2] = "No";
            }
            dtm.addRow(row);
        }
        
    }

