    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tblWFH.getModel();
        dtm.setRowCount(0);
        String txtIP = txtSearch.getText().trim();
        if(!txtIP.isEmpty())
        {
            if(Validate.isIP(txtIP))
            {
                txtSearch.setText("");
                IPConfig ip = system.getRmap().checkIPPresent(txtIP);
                if(ip!=null)
                {
                     Object[] row = new Object[3];
                    row[0] = ip;
                    row[1] = ip.getLocation();
                    if(ip.getHomeip() ==1)
                    {
                        row[2] = "Yes";
                    }
                    else
                    {
                         row[2] = "No";
                    }
                    dtm.addRow(row);
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"No Data found");
                    populateTable();
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Invalid IP");
            }
        }
        else
        {
            populateTable();
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblWFH.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select any row");
            return;
        }
        IPConfig ip = (IPConfig) tblWFH.getValueAt(selectedRow, 0);
        
        system.getRmap().deleteCompleteIP(ip);
       
        JOptionPane.showMessageDialog(null,"IP deleted from network");
        populateTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


